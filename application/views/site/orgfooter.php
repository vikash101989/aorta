<script>
    $('.item_filter').click(function(e){  
        e.preventDefault()
        var specialization = multiple_values('item_filter');
        window.location.href ='<?php echo base_url()?>?specialization=' + specialization;
    });
    
    function multiple_values(inputclass){
        var val = new Array();
        $("."+inputclass+":checked").each(function() {
         val.push($(this).data('field_value'));
        });
        return val.join(',');
    }
function searchFilter(page_num){
    page_num = page_num ? page_num : 0;
    var keywords = $("#search_text").val();
    var sortBy = $('#sortBy').val();
    var specialization = '<?php echo !empty($this->input->get('specialization')) ? $this->input->get('specialization') :'' ?>';
    $.ajax({
        type: 'POST',
        url: '<?php echo base_url('admin/ajaxPaginationData/'); ?>'+page_num,
        data:'page='+page_num+'&keywords='+keywords+'&sortBy='+sortBy,
        beforeSend: function(){
            $('.loading').show();
        },
        success: function(html){
            $('#dataList').html(html);
            $('.loading').fadeOut("slow");
        }
    });
}

